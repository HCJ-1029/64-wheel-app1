/**
 * 根据id获取卦象数据
 * @param {number} id - 卦象id (1-64)
 * @returns {Object|null} 卦象数据对象
 */
export function getGuaById(id) {
  if (id < 1 || id > 64) return null
  return bagua64[id - 1]
}

/**
 * 根据颜色组获取卦象列表
 * @param {number} colorGroup - 颜色组 (1-8)
 * @returns {Array} 该颜色组的卦象数组
 */
export function getGuaByColorGroup(colorGroup) {
  return bagua64.filter(gua => gua.colorGroup === colorGroup)
}

/**
 * 获取所有卦象数据
 * @returns {Array} 完整的64卦数组
 */
export function getAllGua() {
  return bagua64
}

/**
 * 根据二进制表示获取卦象
 * @param {string} binary - 二进制字符串，如 "111111"
 * @returns {Object|null} 卦象数据对象
 */
export function getGuaByBinary(binary) {
  return bagua64.find(gua => gua.binary === binary) || null
}

export default bagua64
